export function normalizeNameKey(name: string): string {
  return name
    .trim()
    .replace(/['\u2019]s$/iu, '')
    .toLowerCase();
}

export function looksLikeNameToken(surface: string): boolean {
  const bare = surface.trim().replace(/['\u2019]s$/u, '');
  if (bare.length < 2) return false;
  return /^\p{Lu}[\p{Ll}\p{M}]+(?:['\u2019]\p{Lu}[\p{Ll}\p{M}]+)*$/u.test(bare);
}
